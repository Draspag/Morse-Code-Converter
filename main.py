def toMorse(totranslate):
    morse_string = ""
    for letter in totranslate:
        morse_letter = morse_alphabet.get(f"{letter.capitalize()}")
        morse_string = morse_string + morse_letter + " "

    return morse_string


print("--- Welcome to the Morse Code Converter ---\n\n")
